package com.multitv.multitvcommonsdk.metadata;

public class ChannelContent {

	private String channelID;
	private String channelName;
	private String channelStreamUrl;

	public String getChannelName() {
		return channelName;
	}

	public void setChannelName(String channelName) {
		this.channelName = channelName;
	}

	public String getChannelID() {
		return channelID;
	}

	public void setChannelID(String channelID) {
		this.channelID = channelID;
	}

	public String getChannelStreamUrl() {
		return channelStreamUrl;
	}

	public void setChannelStreamUrl(String channelStreamUrl) {
		this.channelStreamUrl = channelStreamUrl;
	}

}
